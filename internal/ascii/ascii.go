func PrintHand(hand [][]string) {
	spacer := "   "

	for i := 0; i < len(hand[0]); i++ {
		for j := 0; j < len(hand); j++ {
			fmt.Print(hand[j][i] + spacer)
		}
		fmt.Println()
	}
}
